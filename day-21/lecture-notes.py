# Slicing

piano_keys = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
piano_keys_tuple = ('la', 'mi', 'do', 're', 'mi', 'fa', 'so')

print(piano_keys[2:5])
print(piano_keys[2:])
print(piano_keys[2:5])
print(piano_keys[2:5:2])
print(piano_keys[::2])
print(piano_keys[::-1])
print(piano_keys[:-1])
print(piano_keys_tuple[:-1])